void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_ans(char a, char b, char c)
{
	ft_putchar(a);
	ft_putchar(b);
	ft_putchar(c);
}

void	ft_print_comb(void)
{
	char	num1;
	char	num2;
	char	num3;

	num1 = '0';
	while (num1 <= '7')
	{
		num2 = num1 + 1;
		while (num2 <= '8')
		{
			num3 = num2 + 1;
			while (num3 <= '9')
			{
				ft_ans(num1, num2, num3);
				if (num1 != '7' || num2 != '8' || num3 != '9')
				{
					ft_putchar(',');
					ft_putchar(' ');
				}
				num3++;
			}
			num2++;
		}
		num1++;
	}
}
